//Arithmetic Operators
#include <stdio.h>
int main ()
{
    //(+) Addition
    //(-) Subtraction
    //(*) Multiplication
    //(/) Division
    //(%) Modulus
    //(++, --) Increment, Decrement

    int x = 5, y = 2, z;
    
    printf("x + y = %d\n", z = x + y);
    printf("x - y = %d\n", z = x - y);
    printf("x * y = %d\n", z = x * y);
    printf("x / y = %d\n", z = x / y);
    printf("Modulus = %d", z = x % y);

    return 0;
}